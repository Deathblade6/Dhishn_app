public class News {
    private String mTitle, mMessage;

    public News(String title, String mMessage) {
        mTitle = title;
        this.mMessage = mMessage;
    }

    public String getmMessage() {
        return mMessage;
    }

    public String getmTitle() {
        return mTitle;
    }
}
